// این تابع برای مدیریت درخواست‌های نادرست یا مسیریابی نشده استفاده می‌شود.
function NotFoundHandler(app) {
  // این middleware زمانی فعال می‌شود که هیچ مسیری با درخواست مطابقت نداشته باشد (یعنی مسیریابی پیدا نشود).
  app.use((req, res, next) => {
    // در پاسخ به کاربر، وضعیت 404 (مسیر پیدا نشد) و یک پیام JSON ارسال می‌شود.
    res.status(404).json({
      message: "Not Found Route", // پیام خطا که نشان‌دهنده عدم وجود مسیر درخواست شده است.
    });
  });
}

module.exports = NotFoundHandler;
